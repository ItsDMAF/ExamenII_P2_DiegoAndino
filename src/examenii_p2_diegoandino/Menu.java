    private void jB_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_GuardarActionPerformed
        JFileChooser jfc = new JFileChooser();
        int op = jfc.showSaveDialog(jB_Guardar);
        if (op == JFileChooser.APPROVE_OPTION) {
            try {
                File f = jfc.getSelectedFile();
                if (!f.exists()) {
                    f = new File(f.getPath() + ".dmaf");
                } 
                    String[] path = f.getPath().split("\\.");
                    if (path[path.length - 1].equals("dmaf")) {
                        ProgressBar pb = new ProgressBar(progressBar, ((Deporte) jCB_Principal.getSelectedItem()));
                        pb.start();
                        FileOutputStream out = new FileOutputStream(f, false);
                        ObjectOutputStream output = new ObjectOutputStream(out);
                        output.writeObject(((Deporte) jCB_Principal.getSelectedItem()));
                        out.close();
                        output.close();
                    }else{
                        JOptionPane.showMessageDialog(jfc, "Archivo no valido");
                    }
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jB_GuardarActionPerformed

    private void jB_CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_CargarActionPerformed
        JFileChooser jfc = new JFileChooser();
        int op = jfc.showSaveDialog(jB_Guardar);
        if (op == JFileChooser.APPROVE_OPTION) {
            try {
                File f = jfc.getSelectedFile();
                if (f.exists()) {
                    String[] path = f.getPath().split("\\.");
                    if (path[path.length - 1].equals("dmaf")) {
                        FileInputStream in = new FileInputStream(f);
                        ObjectInputStream input = new ObjectInputStream(in);
                        try {
                            Deporte deporte = (Deporte) input.readObject();
                            deportes.add(deporte);
                            llenarTree();
                        } catch (EOFException ex) {

                        }
                        in.close();
                        input.close();

                    } else {
                        JOptionPane.showMessageDialog(this, "File Not Supported");
                    }

                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        UpdateComboBox();
                                       
    }//GEN-LAST:event_jB_CargarActionPerformed

