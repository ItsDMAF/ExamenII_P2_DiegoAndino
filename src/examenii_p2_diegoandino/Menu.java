    private void jB_CrearEquipMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jB_CrearEquipMouseClicked

        ArrayList<Equipos> equipos = ((Torneo) actualizar.getUserObject()).getEquipos();
        String newName = TF_TeamName.getText();
        int validacion = 0;

        for (Equipos equipo : equipos) {
            if (newName.equals(equipo.getNombre())) {
                validacion++;
                break;
            }
        }

        if (validacion == 0) {
            equipos.add(new Equipos(newName, 0));
            ((Torneo) actualizar.getUserObject()).sortTeams();
            jD_CrearEqui.dispose();
        } else {
            JOptionPane.showMessageDialog(jD_CrearEqui, "Nombre repetido");
        }
    }//GEN-LAST:event_jB_CrearEquipMouseClicked

